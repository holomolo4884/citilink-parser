from pydantic import BaseModel, Field
from typing import Optional, Dict


class Properties(BaseModel):
    """
    Модель данных.
    Pydantic автоматически валидирует типы и преобразует данные.
    """
    
    # ----- БАЗОВЫЕ ПОЛЯ (есть у всех товаров) -----
    
    # Бренд / производитель
    brand: Optional[str] = Field(None, alias="Бренд")
    
    # Модель товара
    model: Optional[str] = Field(None, alias="Модель")
    
    # Цена в рублях
    price: Optional[str] = Field(None, alias="Цена")
    
    # Ссылка на изображение товара
    image_url: Optional[str] = Field(None, alias="Ссылка на изображение")
    
    # ----- ДИНАМИЧЕСКИЕ ХАРАКТЕРИСТИКИ -----
    # Словарь для всех остальных характеристик
    # Ключ - название характеристики (например: "Тип конфорок")
    # Значение - значение характеристики (например: "Hi-Light")
    characteristics: Dict[str, str] = Field(default_factory=dict, alias="Характеристики")
    
    class Config:
        # Разрешаем использовать алиасы (русские названия) при создании объекта
        populate_by_name = True
        
        # Разрешаем дополнительные поля, которых нет в модели
        # Они будут добавлены в словарь characteristics
        extra = 'allow'